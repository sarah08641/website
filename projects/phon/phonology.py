def parse(f,s):
    #Parse sequence s with FSA f
    curr_state = 0
    for sym in s:
        old_state = curr_state
        if(curr_state, sym) not in f: 
            print(str(old_state) + " -> "+sym+" -> ")
            return False # no transition found so we reject
        curr_state = f[(curr_state, sym)]
        print(str(old_state) + " -> "+sym+" -> "+str(curr_state))
    return f[(curr_state, '')]


def phraseParse(f, list):
    for word in list:
        if parse(f,word) == False:
            return False
    
    return True


